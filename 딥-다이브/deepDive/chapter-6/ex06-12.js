

const first = 'LEE';
const last = 'JungHyun';

// ES5: 문자열 연결
console.log('My name is ' + first + ' ' + last + '.');  // My name is LEE JungHyun.
// ES6: 표현식 삽입
console.log(`My name is ${first} ${last}.`);            // My name is LEE JungHyun.

console.log(`1 + 2 = ${1 + 2}`);    // 3
console.log(`2 / 1 = ${2 / 1}`);    // 2
console.log(`3 * 3 = ${3 * 3}`);    // 9




