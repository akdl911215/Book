// 문자열을 정수로 해석하여 반환한다.
console.log(parseInt('10')); // 10
console.log(parseInt('10.123')); // 10

console.log(parseInt(10)); // 10
console.log(parseInt(10.123)); // 10

// 두 번째 인수는 진법을 나타내는 기수(2~36)
// '10'을 10진수로 해석하고 그 결과를 10진수 정수로 반환한다. (default)
